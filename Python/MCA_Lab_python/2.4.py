# Write a python Program to read a number and display corresponding day using if_elif_else?

num = int(input("Enter your number: "))

if (num == 1):
    print("MONDAY")
elif(num == 2):
    print("TUESDAY")
elif(num == 3):
    print("WEDNESDAY")
elif(num == 4):
    print("THURSDAY")
elif(num == 5):
    print("FRIDAY")
elif(num == 6):
    print("SATURDAY")
elif(num == 7):
    print("SUNDAY")
else:
    print("Please enter a valid number ")